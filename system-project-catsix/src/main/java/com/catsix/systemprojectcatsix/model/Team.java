package com.catsix.systemprojectcatsix.model;

public class Team {
    private String team_ID;
    private String team_name;
    private String team_notice_ID;

    public String getTeam_ID() {
        return team_ID;
    }

    public void setTeam_ID(String team_ID) {
        this.team_ID = team_ID;
    }

    public String getTeam_name() {
        return team_name;
    }

    public void setTeam_name(String team_name) {
        this.team_name = team_name;
    }

    public String getTeam_notice_ID() {
        return team_notice_ID;
    }

    public void setTeam_notice_ID(String team_notice_ID) {
        this.team_notice_ID = team_notice_ID;
    }
}
